diaSemana = int(input('Digite um numero: '))

if diaSemana == 1:
    print('Domingo')
elif diaSemana == 2:
    print('Segunda-Feira')
elif diaSemana == 3:
    print('Terça-Feira')
elif diaSemana == 4:
    print('Quarta-Feira')
elif diaSemana == 5:
    print('Quinta-Feira')
elif diaSemana == 6:
    print('Sexta-Feira')
elif diaSemana == 7:
    print('Sábado')
else:
    print('Valor inválido')